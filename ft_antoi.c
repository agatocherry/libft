#include "libft.h"

int	ft_antoi(const char *nptr, int len)
{
	int		i;
	long	nb;

	i = 0;
	nb = 0;
	while (nptr[i] >= '0' && nptr[i] <= '9' && i < len)
	{
		nb = (nb * 10) + (nptr[i] - '0');
		i++;
	}
	return (nb);
}
